const proxyRewrites = [];
Object.keys(proxy).forEach((domain) => {
  proxyRewrites.push({
    has: [{ type: "host", value: domain }],
    source: "/",
    destination: proxy[domain],
  });
  proxyRewrites.push({
    has: [{ type: "host", value: domain }],
    source: "/:path*",
    destination: `${proxy[domain]}/:path*`,
  });
});

